def red_text(text):
    x = f'\033[1;31m{text}\033[m'
    return x


def green_text(text):
    x = f'\033[1;32m{text}\033[m'
    return x


def yellow_text(text):
    x = f'\033[1;33m{text}\033[m'
    return x


def blue_text(text):
    x = f'\033[1;34m{text}\033[m'
    return x

